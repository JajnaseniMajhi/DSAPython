import sys
sys.setrecursionlimit(10**6)
class solution:
    def mergeSort(self,A,s,e):
        if s>=e:
            return 0
        mid= (s+e)//2
        leftPair=self.mergeSort(A,s,mid)%(10**9 +7)
        rightPair=self.mergeSort(A,mid+1,e)%(10**9 +7)
        bothPair=self.merge(A,s,mid,e)
        return (leftPair + rightPair + self.merge(A, s, mid, e)) % (10**9 +7)


    def merge(self,A,s,n,e):
        T=[]
        p1=s
        p2=n+1
        p3=0
        count=0
        while p1<=n and p2<=e:
            if A[p1]<A[p2]:
                T.append(A[p1])
                p1=p1+1
                p3=p3+1

            else:
                T.append(A[p2])
                p2=p2+1
                p3=p3+1
                count+= n-p1+1

        while p1<=n:
            T.append(A[p1])
            p3=p3+1
            p1=p1+1

        while p2<=e:
            T.append(A[p2])
            p3=p3+1
            p2=p2+1
        for i in range(s, e+1):
            A[i] = T[i-s]

        return count %(10**9 +7)

    def solve(self,A):
        s=0
        e=len(A)-1
        return self.mergeSort(A,s,e) %(10**9 +7)


